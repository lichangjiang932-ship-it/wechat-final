const STORAGE_KEY = 'APP_LANG';

function getLang() {
  return wx.getStorageSync(STORAGE_KEY) || 'zh';
}

function setLang(lang) {
  if (lang !== 'zh' && lang !== 'en') lang = 'zh';
  wx.setStorageSync(STORAGE_KEY, lang);
  const app = getApp();
  if (app && app.globalData) app.globalData.lang = lang;
  return lang;
}

function t(key, lang) {
  const L = lang || getLang();
  return (DICT[L] && DICT[L][key]) || (DICT.zh[key]) || key;
}

/** 生成整份字典切片，方便一次性注入到 page.data.i18n */
function pack(lang) {
  const L = lang || getLang();
  return Object.assign({}, DICT.zh, DICT[L] || {});
}

module.exports = { t, getLang, setLang, pack, DICT };
